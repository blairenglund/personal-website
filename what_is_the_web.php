				The World Wide Web is a mainstay of life in the age of technology. It contains every website we visit from Amazon to Zillow and it's only going to continue expanding. It allows us to shop from our homes, read up on just about anything we're interested in, and distract ourselves in countless ways, but what is it? Where did it come from? And how does it actually work?<br><br>

				The World Wide Web is a tool that exists on the Internet. It is not the same thing as the Internet, but it exists as a service on the Internet, like email and FTP. It contains an ever-growing number of text documents as well as the tools that allow us to find and display those documents in our own web browsers.<br><br>

				It was created by British scientist Tim Berners-Lee as a way for CERN physicists to share their research with other scientists across the world. Prior to the creation of the Web, computers connected to the Internet shared information via a number of different protocalls. There was not a simple way of accessing a page with information.<br><br>

				Berners-Lee created the World Wide Web by creating a system for storing and displaying pages of text, and then linking to other pages within the text (called <em>hypertext</em>). These connections form the web part of the World Wide Web. These pages of text contain code called <em>hypertext markup language</em> or HTML, also created by Berners-Lee. HTML is the standard in creating webpages and displaying them correctly on a browser. Berners-Lee and his team at CERN also developed <em>hypertext transfer protocall</em> or HTTP, the system by which we move between pages. The final part of the system developed by Berners-Lee at CERN was to create a URI (<em>universal resource indicator</em>) system he called a URL (yes it is confusing). The <em>universal resource locator</em> is the address we use to find the pages we want to connect to. URLs use a domain and a syntax that defines pages' location with slashes.
